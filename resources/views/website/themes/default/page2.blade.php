			<br/>
سياسة إنهاء الخدمات:
			<br/>
يحق لإيزي منيو تعليق حسابك وإنهاؤه فورًا دون إشعار مسبق بحالة انتهاكك للشروط المذكورة، عند الانهاء سيبطل حقك باستخدام حسابك مرة أخرى وستفقد كافة البيانات المتعلقة به.
