الروابط لمواقع الويب الأخرى:
			<br/>
قد تحتوي خدمتنا على روابط لمواقع أو خدمات ويب تابعة لجهات خارجية لا تملكها إيزي منيو أو لديها الصلاحية للتحكم بها، لذلك لا تتحمل إيزي منيو مسؤولية المحتوى أو سياسيات الخصوصية أو الممارسات التابعة لأيّ موقع أو خدمات الويب الخاصة بطرف ثالث، لذلك أنت تقرّ وتوافق على أن إيزي منيو ليست مسؤولة بشكل مباشر أو غير مباشر عن أيّ ضرر أو خسارة ناتجة عن استخدام أو الاعتماد على أي محتوى أو خدمات أو سلع متاحة على مواقع الويب أو الخدمات المختلفة.
